#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*temp;

	temp = NULL;
	while (*s)
	{
		if (*s == c)
			temp = (char *)s;
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (temp);
}
